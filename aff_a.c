#include <unistd.h>

int	main(int ac, char **av)
{
	char	*str;
	int		i;

	if (ac == 2)
	{
		str = av[1];
		while (*str != '\0')
		{
			if (*str == 'a')
			{
				write(1, "a", 1);
			}
			str++;
		}
		write(1, "\n", 1);
	}
	else
		write (1, "a\n", 2);
	return (0);
}
